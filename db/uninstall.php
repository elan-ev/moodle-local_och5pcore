function xmldb_local_och5p_core_uninstall() {
    global $CFG, $DB;

    require_once($CFG->dirroot . '/local/och5p_core/och5p_core.php');

    $themes = \core_component::get_plugin_list('theme'); 
    if (count($themes)) {
        och5p_core_unenxtend_themes(array_keys($themes));
    }

    return true;
}